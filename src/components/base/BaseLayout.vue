<template>
    <ion-page>
        <ion-header collapse="fade">
            <ion-toolbar>
                <ion-buttons slot="start">
                    <ion-back-button :default-href="pageDefaultBackLink"></ion-back-button>
                </ion-buttons>
                <ion-img :src="tainacanLogo" class="header__tainacan-logo" />
                <ion-buttons slot="end">
                    <ion-button @click="logOff" aria-label="Sair">
                        <ion-icon slot="icon-only" :icon="logOutOutline"></ion-icon>
                    </ion-button>
                </ion-buttons>
            </ion-toolbar>
            <ion-toolbar v-if="pageTitle">
                <ion-title> {{ pageTitle }} </ion-title>
            </ion-toolbar>
        </ion-header>
        <ion-content fullscreen="true">
            <slot />
        </ion-content>
        <ion-toast
            :is-open="tainacanStore.errorStatus"
            :message="$t(tainacanStore.errorMessage)"
            :duration="4000"
            @didDismiss="setOpen(false)"
            color="danger"
        >
        </ion-toast>
    </ion-page>
</template>

<script lang="ts">
import { logOutOutline } from "ionicons/icons";
import { 
    useWpStore
} from '../../store/storeWp';

import {
    IonPage,
    IonHeader,
    IonToolbar,
    IonTitle,
    IonContent,
    IonIcon,
    IonBackButton,
    IonButton,
    IonButtons,
    IonImg,
    IonToast
} from '@ionic/vue';
import { computed } from 'vue';
import { useTainacanStore } from '@/store/storeTainacan';

export default {
    props: ['pageTitle', 'pageDefaultBackLink'],
    components: {
        IonPage,
        IonHeader,
        IonToolbar,
        IonTitle,
        IonContent,
        IonIcon,
        IonButton,
        IonBackButton,
        IonButtons,
        IonImg,
        IonToast
    },
    setup(){
        const tainacanLogo = computed (() => require('../../assets/logo.png'))
        let wpStore = useWpStore();
        let tainacanStore = useTainacanStore();

        return {
            tainacanLogo,
            wpStore,
            logOutOutline,
            tainacanStore
        }  
    },
    methods: {
        async logOff(){
            await this.wpStore.logoff();
            this.$router.go();
        }
    }
}
</script>

<style>
.header__tainacan-logo {
    max-width: 65%;
    height: 42px;
}
ion-img.header__tainacan-logo::part(image) {
    width: auto;
}
</style>